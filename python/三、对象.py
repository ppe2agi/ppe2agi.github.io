# 1、对象
# 1.1 对象是面向对象编程的核心概念，它将数据和操作数据的方法封装在一起。面向对象编程（OOP，Object-Oriented Programming），是一种程序设计思想。相比FP,OOP更抽象，反而更接近人类的认知习惯，更符合我们对现实世界的理解。其实用面向过程编程（POP,Procedure-Oriented Programming）更准确，但函数式编程是在更高解决了过程的副作用问题，所以这里姑且一说。
# 1.2 编程范式
# 命令式编程 (Imperative Programming)，POP (面向过程)属于子类。类比去餐厅吃饭，告诉厨师：“先拿个锅，倒油，烧热，把鸡蛋打进去，翻炒两下，最后加盐出锅。”面向对象编程，OOP，也是命令式编程的一种风格。类比去餐厅吃饭，告诉厨师：“帮我做一份炒鸡蛋，记得用不粘锅和橄榄油。”
# 声明式编程 (Declarative Programming)，FP (函数式)属于子类。类比去餐厅吃饭，告诉厨师：“给我来一份炒鸡蛋。”

